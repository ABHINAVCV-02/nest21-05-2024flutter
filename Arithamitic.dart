void main() {
  // Declare variables for numbers
  int num1 = 10;
  int num2 = 2  ;

  // Perform arithmetic operations
  int sum = num1 + num2;
  int difference = num1 - num2;
  int product = num1 * num2;
  double quotient = num1 / num2; // Use double for division with decimals

  // Print the results
  print("Sum: $sum");
